export default {
  data() {
    return {
      id: 1,
      storeLegalName: "",
      storeVat: "",
      storeAddress: "",
      storeZipCode: "",
      storeCity: "",
      storeEmail: "",
      storePhone: "",
    };
  },
  mounted() {
    this.setIdFromQs();
    this.setStore(this.id);
  },
  methods: {
    setIdFromQs() {
      // Get id from qs url
      const queryString = window.location.search;
      const urlParams = new URLSearchParams(queryString);
      const id = urlParams.get("id");
      // If id is not null, set it to data
      this.id = id || 1;
    },
    setStore(id) {
      this._storeService
        .Get(id)
        .then((store) => {
          this.storeLegalName = store.legalName;
          this.storeVat = store.vat;
          this.storeAddress = store.address.fullAddress;
          this.storeZipCode = store.address.zipCode;
          this.storeCity = store.address.city;
          this.storeEmail = store.email;
          this.storePhone = store.phone;
        })
        .catch((error) => {
          if (id !== 1) {
            this.setStore(1);
          }
        });
    },
  },
};
</script>
